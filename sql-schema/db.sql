/*
  RESUMO DA LÓGICA DO BANCO DE DADOS - SISTEMA DE MONITORAMENTO DE ATIVIDADE FÍSICA

  1. GESTÃO DE USUÁRIOS E PERFIS:
     - Suporta PACIENTES e ADMINISTRADORES. Pacientes possuem perfis detalhados com informações 
       clínicas (tipo de artrite, tempo de diagnóstico).

  2. TRIAGEM E EVOLUÇÃO (sessoes_triagem):
     - Registra scores de diversos instrumentos (IPAQ, FACIT-F, etc.) em fases iniciais e finais
       para mensurar o impacto das intervenções.

  3. HIERARQUIA DE MONITORAMENTO:
     - Relatório Semanal: Agregador principal que consolida o desempenho de uma semana.
     - Relatório Diário: Vinculado a uma semana, registra métricas subjetivas (dor e fadiga).
     - Registro de Atividade: Atividades específicas realizadas no dia, vinculadas ao relatório diário.

  4. LÓGICA DE CÁLCULO E PONTUAÇÃO:
     - MET (Metabolic Equivalent of Task): Calculado com base no valor de referência da atividade
       cadastrada vs. a duração informada pelo usuário.
     - PONTUAÇÃO: Sistema de pesos por intensidade para gamificação/metas:
       (Minutos Leves * 1) + (Minutos Moderados * 2) + (Minutos Intensos * 3).

  5. REGRAS DE NEGÓCIO:
     - Prazo de Edição: Centralizado na função 'prazo_edicao_config', limitando alterações 
       retroativas (ex: 72h) para garantir a integridade dos dados coletados.
*/

