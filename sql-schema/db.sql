-- Função para centralizar o prazo de edição (atua como uma variável global)
CREATE OR REPLACE FUNCTION prazo_edicao_config() RETURNS interval AS $$
BEGIN
  RETURN interval '72 hours';
END;
$$ LANGUAGE plpgsql;

