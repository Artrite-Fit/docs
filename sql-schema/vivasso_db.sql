-- 5. Monitoramento de Atividade
CREATE TABLE "atividade_cadastrada" (
  "id_atividade_cadastrada" INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  "nome" TEXT NOT NULL,
  "categoria" TEXT,
  "MET_leve" FLOAT NOT NULL,
  "MET_moderado" FLOAT NOT NULL,
  "MET_intenso" FLOAT NOT NULL,
  "ativo" Boolean NOT NULL DEFAULT true,
  "criado_em" TIMESTAMP NOT NULL DEFAULT (now())
);

CREATE TABLE "relatorio" (
  "id_relatorio" INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  "usuario_id" Int NOT NULL,
  "data_referencia" DATE NOT NULL,
  "turno" turno_dia NOT NULL,
  "fadiga_percebida" Int,
  "observacoes" TEXT,
  "minutos_leve" Int NOT NULL DEFAULT 0,
  "minutos_moderado" Int NOT NULL DEFAULT 0,
  "minutos_intenso" Int NOT NULL DEFAULT 0,
  "pontuacao" Int NOT NULL DEFAULT 0,
  "met_total" Decimal NOT NULL DEFAULT 0,
  "eva_media" Int CHECK ("eva_media" >= 0 AND "eva_media" <= 10),
  "editavel_ate" TIMESTAMP NOT NULL,
  "criado_em" TIMESTAMP NOT NULL DEFAULT (now()),
  "atualizado_em" TIMESTAMP NOT NULL DEFAULT (now()),
  FOREIGN KEY ("usuario_id") REFERENCES "usuario" ("id_usuario"),
  UNIQUE ("usuario_id", "data_referencia", "turno")
);

CREATE TABLE "registro_atividade" (
  "id_registro_atividade" INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  "relatorio_id" Int NOT NULL,
  "atividade_cadastrada_id" Int NOT NULL,
  "duracao_minutos" Int NOT NULL,
  "intensidade_percebida" intensidade_atividade NOT NULL,
  "resultado_met" Decimal NOT NULL,
  "realizada_em" TIMESTAMP NOT NULL,
  "eva_dor" Int CHECK ("eva_dor" >= 0 AND "eva_dor" <= 10),
  "criado_em" TIMESTAMP NOT NULL DEFAULT (now()),
  "atualizado_em" TIMESTAMP NOT NULL DEFAULT (now()),
  FOREIGN KEY ("relatorio_id") REFERENCES "relatorio" ("id_relatorio"),
  FOREIGN KEY ("atividade_cadastrada_id") REFERENCES "atividade_cadastrada" ("id_atividade_cadastrada")
);

-- 6. Engajamento e Conteúdo
