/*
  SISTEMA DE MONITORAMENTO DE ATIVIDADE FÍSICA - SCHEMA SIMPLIFICADO (SINGULAR)
  Versão: Consolidação de Relatórios e Notificações Externas.
*/

-- 1. Definição de Tipos Enumerados
CREATE TYPE "papel_usuario" AS ENUM (
  'PACIENTE',
  'ADMINISTRADOR'
);

CREATE TYPE "fase_triagem" AS ENUM (
  'INICIAL',
  'FINAL'
);

CREATE TYPE "intensidade_atividade" AS ENUM (
  'LEVE',
  'MODERADO',
  'INTENSO'
);

CREATE TYPE "turno_dia" AS ENUM (
  'MANHA',
  'NOITE'
);

-- 2. Configurações Globais
CREATE OR REPLACE FUNCTION prazo_edicao_config() RETURNS interval AS $$
BEGIN
  RETURN interval '72 hours';
END;
$$ LANGUAGE plpgsql;

-- 3. Tabelas de Identidade e Perfis
CREATE TABLE "usuario" (
  "id_usuario" INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  "nome" TEXT NOT NULL,
  "email" TEXT UNIQUE NOT NULL,
  "telefone" TEXT NOT NULL,
  "data_nascimento" DATE NOT NULL,
  "senha_hash" TEXT,
  "google_id" TEXT UNIQUE,
  "papel" papel_usuario NOT NULL DEFAULT 'PACIENTE',
  "email_verificado" Boolean NOT NULL DEFAULT false,
  "ativo" Boolean NOT NULL DEFAULT true,
  "criado_em" TIMESTAMP NOT NULL DEFAULT (now()),
  "atualizado_em" TIMESTAMP NOT NULL DEFAULT (now())
);

CREATE TABLE "tipo_artrite" (
  "id_tipo_artrite" INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  "nome" TEXT UNIQUE NOT NULL,
  "ativo" Boolean NOT NULL DEFAULT true,
  "criado_em" TIMESTAMP NOT NULL DEFAULT (now())
);

CREATE TABLE "perfil_paciente" (
  "id_perfil_paciente" INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  "usuario_id" Int UNIQUE NOT NULL,
  "sexo" TEXT NOT NULL,
  "escolaridade" TEXT NOT NULL,
  "raca_cor" TEXT NOT NULL,
  "estado_civil" TEXT NOT NULL,
  "tipo_artrite_id" Int NOT NULL,
  "tempo_tratamento_meses" Int,
  "tempo_diagnostico_meses" Int,
  -- Colunas movidas da tabela usuario
  "lgpd_aceita_em" TIMESTAMP,
  "consentimento_saude_em" TIMESTAMP,
  "criado_em" TIMESTAMP NOT NULL DEFAULT (now()),
  "atualizado_em" TIMESTAMP NOT NULL DEFAULT (now()),
  FOREIGN KEY ("usuario_id") REFERENCES "usuario" ("id_usuario"),
  FOREIGN KEY ("tipo_artrite_id") REFERENCES "tipo_artrite" ("id_tipo_artrite")
);

-- 4. Avaliação Clínica
CREATE TABLE "sessao_triagem" (
  "id_sessao_triagem" INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  "usuario_id" Int NOT NULL,
  "fase" fase_triagem NOT NULL,
  "score_ipaq" Decimal,
  "score_facit_f" Decimal,
  "score_pam13" Decimal,
  "score_sam" Decimal,
  "score_sure" Decimal,
  "score_umars" Decimal,
  "score_eheals" Decimal,
  "respostas_brutas" Json,
